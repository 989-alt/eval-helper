  '매우잘함': '개념을 정확하게 이해하고 다양한 상황에 능숙하게 적용하며 과정과 결과를 논리적으로 설명하거나 친구를 도와 이끎. 훌륭히, 정확하게, 구체적으로, 능숙하게, 창의적으로, 논리적으로, 스스로, 적극적으로 같은 표현으로 완전한 이해와 확장, 주도성을 드러낸다.',
