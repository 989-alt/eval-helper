  '2. 모든 문장은 명사형으로 끝낸다(~함. ~임. ~됨.). ~했습니다, ~한다, ~하였음 같은 서술형이나 과거형 어미를 쓰지 않는다.',
  '3. 기업, 제품, 서비스 이름 같은 특정 상표명은 쓰지 않고 일반명사로 바꾼다. 다만 학습 내용의 핵심 개념(인명, 작품명, 역사 사건명 등)은 그대로 쓴다.',
  '4. 주어진 평가요소나 특성에 실제로 있는 표현만 조합해 쓴다. 평가 내용에 없는 소재나 예시를 임의로 지어내지 않는다.',
